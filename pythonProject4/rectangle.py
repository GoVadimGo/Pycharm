# модуль rectangle содержит функции , которые вычисляют площадь и периметр прямоугольника

# функции area принимает в качестве аргумента длину и ширину прямоугольника и возвращает площаль прямоугольника



def area(width,length):
    return width * length

# функция perimetr принимает в качестве аргумента длину и ширину прямоугольника и возвращает его периметр

def perimetr(width,length):
    return 2 * (width + length)
